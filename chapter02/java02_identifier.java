package chapter02;

public class java02_identifier {
    public static void main(String[] args) {
        //identifier is for nonaming
        //1. using alphabet
        String name = "Xiao";
        System.out.println(name);

        //2. special character: only _ and  $ are allowed
        String $pr_ice = "five";
        System.out.println($pr_ice);

        //3. number, but not start
        String name2 = "Yun";
        System.out.println(name2);

        //4. inside {}, no double idnetifider 
        String name3 = "Malta";
        System.out.println(name3);

        //5. identifier is case sensitive
        String Name = "Google";
        System.out.println(Name);

        // 6. identifier's length is unlimited



        
    }
    
}
